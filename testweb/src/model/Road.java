package model;

public class Road {
	private String roadID;
	private String roadName;
	private String roadMPoint;
	private String roadCPoint;
	private String roadPoint;
	private String roadpointname;
	
	public String getRoadID() {
		return roadID;
	}
	public void setRoadID(String roadID) {
		this.roadID = roadID;
	}
	public String getRoadName() {
		return roadName;
	}
	public void setRoadName(String roadName) {
		this.roadName = roadName;
	}
	public String getRoadPoint() {
		return roadPoint;
	}
	public void setRoadPoint(String roadPoint) {
		this.roadPoint = roadPoint;
	}
	public String getRoadMPoint() {
		return roadMPoint;
	}
	public void setRoadMPoint(String roadMPoint) {
		this.roadMPoint = roadMPoint;
	}
	public String getRoadCPoint() {
		return roadCPoint;
	}
	public void setRoadCPoint(String roadCPoint) {
		this.roadCPoint = roadCPoint;
	}
	public String getRoadpointname() {
		return roadpointname;
	}
	public void setRoadpointname(String roadpointname) {
		this.roadpointname = roadpointname;
	}

}
