package model;

public class Equipment {

	private String EquipID;
	private int EquipState;
	private String EquipBPoint;
	private String EquipName;
	private String pointname;
	public String getEquipID() {
		return EquipID;
	}
	public void setEquipID(String equipID) {
		EquipID = equipID;
	}
	public int getEquipState() {
		return EquipState;
	}
	public void setEquipState(int equipState) {
		EquipState = equipState;
	}
	public String getEquipBPoint() {
		return EquipBPoint;
	}
	public void setEquipBPoint(String equipBPoint) {
		EquipBPoint = equipBPoint;
	}
	public String getEquipName() {
		return EquipName;
	}
	public void setEquipName(String setEquipName) {
		this.EquipName = setEquipName;
	}
	public String getPointname() {
		return pointname;
	}
	public void setPointname(String pointname) {
		this.pointname = pointname;
	}
	

	
}
